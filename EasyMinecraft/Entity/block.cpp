block::block(glm::vec3 pos, std::string na, Camera* ca,Shader* sha)
{
	
	Position = pos;
	name = na;
	camera = ca;
	shader = sha;
	ID = shader->getID();

	glGenBuffers(1, VBO);
	glBindBuffer(GL_ARRAY_BUFFER, VBO[0]);
	glBufferData(GL_ARRAY_BUFFER, sizeof(vertices), vertices, GL_STREAM_DRAW);


	glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 5 * sizeof(float), (void*)0); //Î»ÖÃ
	glEnableVertexAttribArray(0);
	glVertexAttribPointer(2, 2, GL_FLOAT, GL_FALSE, 5 * sizeof(float), (void*)(3 * sizeof(float))); //ÑÕÉ«
	glEnableVertexAttribArray(2);

	
	glGenTextures(2, TexBuffer);

	glActiveTexture(GL_TEXTURE0);
	std::string file = "./Texture/blocks/gress.png";
	MakeTextureBuffer(TexBuffer[0], file.data());

	modelMat = glm::translate(glm::mat4(1.0f), Position);
	proMat = camera->proMat;
}

